#!/usr/bin/env bash
# Generate dataset_catalog.md from JSON inventory files
# Similar to tools/update_apps_catalog.sh

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
JSON_DIR="$SCRIPT_DIR/dataset_inventory"
CATALOG_FILE="$SCRIPT_DIR/../docs/datasets/dataset_catalog.md"
OUT_DIR="$SCRIPT_DIR/../docs/datasets"

mkdir -p "$(dirname "$CATALOG_FILE")"
mkdir -p "$OUT_DIR"

# Stats
today=$(date +"%B %d, %Y")

# Count datasets from all JSON files
dataset_count=0
for json_file in "$JSON_DIR"/*.json; do
    [ -f "$json_file" ] || continue
    count=$(jq '[to_entries[] | .value | keys[]] | length' "$json_file" 2>/dev/null || echo 0)
    dataset_count=$((dataset_count + count))
done

# Get unique categories
categories="AI, Climate Model, Covariates, GeoAI, Geospatial, Hydrological, Meteorological"
category_count=7

{
  echo "---"
  echo "hide:"
  echo "  - footer"
  echo "  - toc"
  echo "search:"
  echo "  exclude: true"
  echo "---"
  echo
  echo "<!-- Note: this catalog file is generated by tools/update_dataset_catalog.sh. Manual changes will be lost!-->"
  echo
  echo "# RCAC Datasets Catalog"
  echo
  echo "## Overview"
  echo "As of **$today**, there are **$dataset_count** publicly available datasets across **$category_count** categories: **$categories**."
  echo
  echo "## Dataset Catalog"
  echo
  echo '<table id="datasetTable" class="tablefilter">'
  echo "  <thead>"
  echo '    <tr><th scope="col"><strong>Dataset</strong></th><th scope="col"><strong>Category</strong></th><th scope="col"><strong>Discipline</strong></th></tr>'
  echo "  </thead>"
  echo "  <tbody>"
  # Helper function to convert discipline from " / " separated to ", " separated
  format_discipline() {
    local raw="$1"
    # Replace " / " with ", " for consistent display and filtering
    echo "$raw" | sed 's/ \/ /, /g'
  }

  # Process ai.json
  if [ -f "$JSON_DIR/ai.json" ]; then
    jq -r 'to_entries[] | .value | to_entries[] | [.key, .value.title, .value.discipline] | @tsv' "$JSON_DIR/ai.json" 2>/dev/null | while IFS=$'\t' read -r key title discipline; do
      [ -z "$key" ] && continue
      formatted_discipline=$(format_discipline "$discipline")
      echo '    <tr>'
      echo '      <td><a href="../ai/'"$key"'"><strong>'"${title:-$key}"'</strong></a></td>'
      echo '      <td>AI</td>'
      echo '      <td>'"${formatted_discipline:-}"'</td>'
      echo '    </tr>'
    done
  fi

  # Process climateModel.json
  if [ -f "$JSON_DIR/climateModel.json" ]; then
    jq -r 'to_entries[] | .value | to_entries[] | [.key, .value.title, .value.discipline] | @tsv' "$JSON_DIR/climateModel.json" 2>/dev/null | while IFS=$'\t' read -r key title discipline; do
      [ -z "$key" ] && continue
      formatted_discipline=$(format_discipline "$discipline")
      echo '    <tr>'
      echo '      <td><a href="../climateModel/'"$key"'"><strong>'"${title:-$key}"'</strong></a></td>'
      echo '      <td>Climate Model</td>'
      echo '      <td>'"${formatted_discipline:-}"'</td>'
      echo '    </tr>'
    done
  fi

  # Process covariates.json
  if [ -f "$JSON_DIR/covariates.json" ]; then
    jq -r 'to_entries[] | .value | to_entries[] | [.key, .value.title, .value.discipline] | @tsv' "$JSON_DIR/covariates.json" 2>/dev/null | while IFS=$'\t' read -r key title discipline; do
      [ -z "$key" ] && continue
      formatted_discipline=$(format_discipline "$discipline")
      echo '    <tr>'
      echo '      <td><a href="../Covariates/'"$key"'"><strong>'"${title:-$key}"'</strong></a></td>'
      echo '      <td>Covariates</td>'
      echo '      <td>'"${formatted_discipline:-}"'</td>'
      echo '    </tr>'
    done
  fi

  # Process geoai.json
  if [ -f "$JSON_DIR/geoai.json" ]; then
    jq -r 'to_entries[] | .value | to_entries[] | [.key, .value.title, .value.discipline] | @tsv' "$JSON_DIR/geoai.json" 2>/dev/null | while IFS=$'\t' read -r key title discipline; do
      [ -z "$key" ] && continue
      formatted_discipline=$(format_discipline "$discipline")
      echo '    <tr>'
      echo '      <td><a href="../geoai/'"$key"'"><strong>'"${title:-$key}"'</strong></a></td>'
      echo '      <td>GeoAI</td>'
      echo '      <td>'"${formatted_discipline:-}"'</td>'
      echo '    </tr>'
    done
  fi

  # Process met-hyd-geo.json
  if [ -f "$JSON_DIR/met-hyd-geo.json" ]; then
    # Meteorological
    jq -r '.meteorological | to_entries[] | [.key, .value.title, .value.discipline] | @tsv' "$JSON_DIR/met-hyd-geo.json" 2>/dev/null | while IFS=$'\t' read -r key title discipline; do
      [ -z "$key" ] && continue
      formatted_discipline=$(format_discipline "$discipline")
      echo '    <tr>'
      echo '      <td><a href="../meteorological/'"$key"'"><strong>'"${title:-$key}"'</strong></a></td>'
      echo '      <td>Meteorological</td>'
      echo '      <td>'"${formatted_discipline:-}"'</td>'
      echo '    </tr>'
    done

    # Geospatial
    jq -r '.geospatial | to_entries[] | [.key, .value.title, .value.discipline] | @tsv' "$JSON_DIR/met-hyd-geo.json" 2>/dev/null | while IFS=$'\t' read -r key title discipline; do
      [ -z "$key" ] && continue
      formatted_discipline=$(format_discipline "$discipline")
      echo '    <tr>'
      echo '      <td><a href="../geospatial/'"$key"'"><strong>'"${title:-$key}"'</strong></a></td>'
      echo '      <td>Geospatial</td>'
      echo '      <td>'"${formatted_discipline:-}"'</td>'
      echo '    </tr>'
    done

    # Hydrological
    jq -r '.hydrological | to_entries[] | [.key, .value.title, .value.discipline] | @tsv' "$JSON_DIR/met-hyd-geo.json" 2>/dev/null | while IFS=$'\t' read -r key title discipline; do
      [ -z "$key" ] && continue
      formatted_discipline=$(format_discipline "$discipline")
      echo '    <tr>'
      echo '      <td><a href="../hydrological/'"$key"'"><strong>'"${title:-$key}"'</strong></a></td>'
      echo '      <td>Hydrological</td>'
      echo '      <td>'"${formatted_discipline:-}"'</td>'
      echo '    </tr>'
    done
  fi

  echo "  </tbody>"
  echo "</table>"

} > "$CATALOG_FILE"

echo "Dataset catalog updated at $CATALOG_FILE with $dataset_count datasets."
